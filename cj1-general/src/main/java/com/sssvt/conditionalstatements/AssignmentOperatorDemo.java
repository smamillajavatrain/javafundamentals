package com.sssvt.conditionalstatements;

/**
 * @author smamilla
 *
 */
public class AssignmentOperatorDemo {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int a = 12, b = 5;
		a+=2;
		b-=3;
		a*=1;
		b/=1;
		b%=2;
		
		System.out.println(a);
		System.out.println(b);
	}

}
