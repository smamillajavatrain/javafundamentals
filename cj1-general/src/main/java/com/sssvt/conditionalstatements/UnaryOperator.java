package com.sssvt.conditionalstatements;

/**
 * @author smamilla
 *
 */
public class UnaryOperator {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int a = 5;
		System.out.println(++a); // a = 6
		System.out.println(a++); // a = 7
		System.out.println(a);  // a = 7
		
		System.out.println(--a);  // a = 6
		System.out.println(a--); // a = 6
		System.out.println(a);   // a = 5
	}

}
